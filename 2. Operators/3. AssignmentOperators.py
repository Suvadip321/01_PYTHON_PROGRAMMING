# --- ASSIGNMENT OPERATORS ---

# Simple assignment (=)
x = 10
print("x =", x)
# Output: x = 10

# Add and assign (+=)
x += 5   # equivalent to x = x + 5
print("x after += 5:", x)
# Output: x after += 5: 15

# Subtract and assign (-=)
x -= 3   # equivalent to x = x - 3
print("x after -= 3:", x)
# Output: x after -= 3: 12

# Multiply and assign (*=)
x *= 2   # equivalent to x = x * 2
print("x after *= 2:", x)
# Output: x after *= 2: 24

# Divide and assign (/=)
x /= 4   # equivalent to x = x / 4
print("x after /= 4:", x)
# Output: x after /= 4: 6.0   (always float)

# Floor divide and assign (//=)
x //= 2  # equivalent to x = x // 2
print("x after //= 2:", x)
# Output: x after //= 2: 3.0

# Modulus and assign (%=)
x %= 2   # equivalent to x = x % 2
print("x after %= 2:", x)
# Output: x after %= 2: 1.0

# Exponent and assign (**=)
x **= 3  # equivalent to x = x ** 3
print("x after **= 3:", x)
# Output: x after **= 3: 1.0