# ARITHMETIC OPERATORS---

a = 15
b = 4

addition = a + b
print(addition)

subtraction = a - b
print(subtraction)

multiplication = a * b
print(multiplication)

division = a / b
print(division)

floor_division = a // b
print(floor_division)

modulus = a % b
print(modulus)

power = a**b
print(power)
