# --- ARITHMETIC OPERATORS ---

a = 15
b = 4

# Addition (+)
print("a + b =", a + b)
# Output: a + b = 19

# Subtraction (-)
print("a - b =", a - b)
# Output: a - b = 11

# Multiplication (*)
print("a * b =", a * b)
# Output: a * b = 60

# Division (/)
# Always returns a float, even if divisible
print("a / b =", a / b)
# Output: a / b = 3.75

# Floor Division (//)
# Returns the quotient without the decimal part (integer division)
print("a // b =", a // b)
# Output: a // b = 3

# Modulus (%)
# Returns the remainder after division
print("a % b =", a % b)
# Output: a % b = 3

# Exponentiation (**)
# Raises a to the power of b
print("a ** b =", a ** b)
# Output: a ** b = 50625
