# --- RELATIONAL (COMPARISON) OPERATORS ---

a = 10
b = 20

# Equal to (==)
print("a == b:", a == b)
# Output: a == b: False

# Not equal to (!=)
print("a != b:", a != b)
# Output: a != b: True

# Greater than (>)
print("a > b:", a > b)
# Output: a > b: False

# Less than (<)
print("a < b:", a < b)
# Output: a < b: True

# Greater than or equal to (>=)
print("a >= b:", a >= b)
# Output: a >= b: False

# Less than or equal to (<=)
print("a <= b:", a <= b)
# Output: a <= b: True


# # --- EXTRA: Comparing different types ---
print("5 == 5.0:", 5 == 5.0)      # int and float comparison
print("True == 1:", True == 1)    # bool and int comparison
print("False == 0:", False == 0)
# Output:
# 5 == 5.0: True
# True == 1: True
# False == 0: True
